// Calculate Attendance Percentage for a single student or all
exports.getAttendancePercentage = async (req, res) => {
    try {
        const { studentId } = req.params;
        const query = studentId ? { studentId } : {};

        const totalDays = await Attendance.countDocuments(query);
        const presentDays = await Attendance.countDocuments({ ...query, status: 'Present' });

        const percentage = totalDays > 0 ? (presentDays / totalDays) * 100 : 0;

        res.status(200).json({ studentId, totalDays, presentDays, percentage });
    } catch (error) {
        res.status(500).json({ message: 'Error calculating percentage.', error: error.message });
    }
};

// Get Dashboard Stats (Analytics for Admin)
exports.getAdminStats = async (req, res) => {
    try {
        const totalStudents = await User.countDocuments({ role: 'student' });
        const pendingLeaveRequests = await Leave.countDocuments({ status: 'Pending' });
        const totalFines = await Fine.aggregate([{ $group: { _id: null, total: { $sum: '$amount' } } }]);

        // Analytics for bar/pie charts (simplified)
        const attendanceTrend = await Attendance.aggregate([
            { $group: { _id: '$status', count: { $sum: 1 } } }
        ]);

        res.status(200).json({
            totalStudents,
            pendingLeaveRequests,
            totalFineAmount: totalFines[0]?.total || 0,
            attendanceTrend
        });
    } catch (error) {
        res.status(500).json({ error: error.message });
    }
};

const { exportToCSV } = require('../utils/csvExporter');

// Get Monthly Report (JSON or CSV)
exports.getMonthlyReport = async (req, res) => {
    try {
        const { month, year, format } = req.query; // format=csv or json
        const startDate = new Date(year, month - 1, 1);
        const endDate = new Date(year, month, 0);

        const reportData = await Attendance.find({
            date: { $gte: startDate, $lte: endDate }
        }).populate('studentId', 'name registerNumber');

        if (format === 'csv') {
            const csvData = reportData.map(r => ({
                StudentName: r.studentId.name,
                RegisterNumber: r.studentId.registerNumber,
                Date: r.date.toDateString(),
                Status: r.status
            }));
            return exportToCSV(res, csvData, `Monthly_Report_${month}_${year}.csv`);
        }

        res.status(200).json(reportData);
    } catch (error) {
        res.status(500).json({ error: error.message });
    }
};

