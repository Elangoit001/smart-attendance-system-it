const Attendance = require('../models/Attendance');
const Leave = require('../models/Leave');
const Fine = require('../models/Fine');
const User = require('../models/User');
const { sendWhatsAppNotification } = require('../utils/notification');

// Mark Daily Attendance (Admin)
exports.markAttendance = async (req, res) => {
    try {
        const { records } = req.body; // records: [{ studentId, status, date }]
        console.log(`[ATTENDANCE] Received ${records?.length || 0} records.`);

        for (const data of records) {
            const { studentId, status, date } = data;

            // Normalize to UTC Midnight regardless of input format
            const d = new Date(date);
            const utcDate = new Date(Date.UTC(d.getFullYear(), d.getMonth(), d.getDate()));

            console.log(`[ATTENDANCE] Processing: ${studentId} -> ${status} on ${utcDate.toISOString()}`);

            // Mark attendance
            await Attendance.findOneAndUpdate(
                { studentId, date: utcDate },
                { status },
                { upsert: true, new: true }
            );

            // Special Automation Feature: Unauthorized Leave Detection
            if (status === 'Absent') {
                const leaveRequest = await Leave.findOne({
                    studentId,
                    status: 'Approved',
                    startDate: { $lte: utcDate },
                    endDate: { $gte: utcDate }
                });

                if (!leaveRequest) {
                    const fineReason = `Unauthorized Absence on ${utcDate.toISOString().split('T')[0]}`;
                    const existingFine = await Fine.findOne({ studentId, reason: fineReason });

                    if (!existingFine) {
                        const fine = new Fine({
                            studentId,
                            reason: fineReason,
                            amount: 50,
                            autoGenerated: true,
                            date: utcDate
                        });
                        await fine.save();

                        const student = await User.findById(studentId);
                        if (student) {
                            const adminMessage = `Alert: Student ${student.name} [Reg: ${student.registerNumber}] was absent without approved leave. Automated fine issued.`;
                            await sendWhatsAppNotification('AdminPhone_placeholder', adminMessage);
                        }
                    }
                }
            }
        }

        res.status(200).json({ message: 'Attendance marked successfully.' });
    } catch (error) {
        console.error('Attendance Mark Error:', error);
        res.status(500).json({ message: 'Error marking attendance.', error: error.message });
    }
};

