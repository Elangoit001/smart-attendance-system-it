// Get Daily Summaries for History (Admin)
exports.getDailySummaries = async (req, res) => {
    try {
        const summaries = await Attendance.aggregate([
            {
                $lookup: {
                    from: 'users',
                    localField: 'studentId',
                    foreignField: '_id',
                    as: 'student'
                }
            },
            { $unwind: '$student' },
            {
                $group: {
                    _id: {
                        date: "$date",
                        year: "$student.year"
                    },
                    presentCount: { $sum: { $cond: [{ $eq: ["$status", "Present"] }, 1, 0] } },
                    absentCount: { $sum: { $cond: [{ $eq: ["$status", "Absent"] }, 1, 0] } }
                }
            },
            {
                $project: {
                    _id: 0,
                    date: "$_id.date",
                    year: "$_id.year",
                    presentCount: 1,
                    absentCount: 1
                }
            },
            { $sort: { date: -1, year: 1 } }
        ]);
        res.status(200).json(summaries);
    } catch (error) {
        res.status(500).json({ error: error.message });
    }
};
